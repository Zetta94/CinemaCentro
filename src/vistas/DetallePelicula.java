    private void obtenerProyecciones() {
        proyecciones = proyeccionData.listarProyeccionesPorId(pelicula.getIdPelicula());
    }

    private void obtenerLugares() {
        if (proyecciones.size() > 0) {
            for (Proyeccion p : proyecciones) {
                Integer lugares = lugaresData.ObtenerLugaresDisponibles(p.getIdProyeccion());
                lugaresDisponibles.put(p, lugares);
            }
        }
    }

